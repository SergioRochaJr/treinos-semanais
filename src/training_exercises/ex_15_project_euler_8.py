from math import prod


def ex15(number: str, adjacent_limit: int) -> int:
    """Find the greatest product of adjacent digits in a number string.

    Scans through the number string to find the maximum product of a sequence
    of adjacent digits with length equal to adjacent_limit. Uses optimization
    to skip past zeros, which cannot contribute to the maximum product.

    Args:
        number: A string of digits to search through.
        adjacent_limit: The number of adjacent digits to multiply together.

    Returns:
        The highest product of adjacent_limit consecutive digits.
    """
    highest_product = 0
    i = 0
    while i <= len(number) - adjacent_limit:
        arranjement = number[i : i + adjacent_limit]
        if "0" in arranjement:
            i += arranjement.rfind("0") + 1
            continue
        highest_product = max(highest_product, prod(int(n) for n in arranjement))
        i += 1
    return highest_product
